glm::mat4 lookAt(const glm::vec3& cameraPos, const glm::vec3& cameraTarget , const glm::vec3& up) {
	glm::vec3 cameraDirection = glm::normalize(cameraPos - cameraTarget);
	glm::vec3 cameraRight = glm::normalize(glm::cross(up, cameraDirection));
	if (glm::length(cameraRight) < 1e-6f) // …Ë÷√“ª∏ˆ–°µƒ„–÷µ
	{
		// »Áπ˚∆Ω––£¨ π”√±∏”√…œœÚ¡ø£®¿˝»Á ¿ΩÁø’º‰µƒz÷·’˝œÚ£©
		cameraRight = glm::vec3(0.0f, 0.0f, 1.0f);
	}
	glm::vec3 cameraUp = glm::cross(cameraDirection, cameraRight);

	// ππ‘Ï ”Õºæÿ’Û£®¡–÷˜–Ú£©
	glm::mat4 viewMatrix(1.0f); // ≥ı ºªØŒ™µ•Œªæÿ’Û

	// …Ë÷√µ⁄“ª¡– (R)
	viewMatrix[0][0] = cameraRight.x; // row0 col0
	viewMatrix[0][1] = cameraRight.y; // row1 col0
	viewMatrix[0][2] = cameraRight.z; // row2 col0
	viewMatrix[0][3] = 0.0f; // row3 col0

	// …Ë÷√µ⁄∂˛¡– (U)
	viewMatrix[1][0] = cameraUp.x; // row0 col1
	viewMatrix[1][1] = cameraUp.y; // row1 col1
	viewMatrix[1][2] = cameraUp.z; // row2 col1
	viewMatrix[1][3] = 0.0f; // row3 col1

	// …Ë÷√µ⁄»˝¡– (-D)
	viewMatrix[2][0] = -cameraDirection.x; // row0 col2
	viewMatrix[2][1] = -cameraDirection.y; // row1 col2
	viewMatrix[2][2] = -cameraDirection.z; // row2 col2
	viewMatrix[2][3] = 0.0f; // row3 col2

	// …Ë÷√µ⁄Àƒ¡– (∆Ω“∆)
	viewMatrix[3][0] = -glm::dot(cameraRight, cameraPos); // row0 col3
	viewMatrix[3][1] = -glm::dot(cameraUp, cameraPos); // row1 col3
	viewMatrix[3][2] = glm::dot(cameraDirection, cameraPos); // row2 col3
//	viewMatrix[3][3] = 1.0f; // row3 col3

	return viewMatrix;
}

